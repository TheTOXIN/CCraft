Chunck::Chunck()
{
	for (int i = 0; i < h; i++)
	{
		for (int j = 0; j < w; j++)
		{
			Block block(0);
			blocks[i][j] = block;
		}
	}
}

void Chunck::generate()
{
	for (int i = 0; i < h; i++)
	{
		for (int j = 0; j < w; j++)
		{
			Block block(208);
			blocks[i][j] = block;
		}
	}
}

